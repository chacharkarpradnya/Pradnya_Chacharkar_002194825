    private void buttnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttnSaveActionPerformed
        // TODO add your handling code here:
        
        try{
            
            if(textBrnd.getText().equals("") || textCity.getText().equals("") || textModelNo.getText().equals(""))
        JOptionPane.showMessageDialog(null,"Fields cannot be empty");
            
            else{
        c.setBrnd(textBrnd.getText());
        c.setManacyear(Integer.parseInt(textYrOfMfg.getText()));
        c.setAvailbilityCity(textCity.getText());
        c.setMaxSeating(Integer.parseInt(textMaxSeating.getText()));
        c.setMinSeating(Integer.parseInt(textMinSeating.getText()));
        if(rbNoAvail.isSelected()==true)
            c.setAvb(false);
        if(rbYesAvail.isSelected()==true)
            c.setAvb(true);
        if(rbNoMaint.isSelected()==true)
            c.setMaint_cert(false);
        if(rbYesMaint.isSelected()==true)
            c.setMaint_cert(true);
        c.setModelNo(textModelNo.getText());
        c.setSerialNo(Integer.parseInt(textSerialNo.getText()));
        populateTable();
        JOptionPane.showMessageDialog(null,"Car Specifics Updated Successfully");
        buttnUpdate.setEnabled(true);
        buttnSave.setEnabled(false);
         textBrnd.setEnabled(false);
        textCity.setEnabled(false);
        textMaxSeating.setEnabled(false);
        textMinSeating.setEnabled(false);
        textModelNo.setEnabled(false);
        textSerialNo.setEnabled(false);
        textYrOfMfg.setEnabled(false);
        rbNoAvail.setEnabled(false);
        rbNoMaint.setEnabled(false);
        rbYesAvail.setEnabled(false);
        rbYesMaint.setEnabled(false);
        date =new Date();
        long Timme = date.getTime();
        timestamp= new Timestamp(Timme);
        Time= String.valueOf(timestamp);
        
        lblTime.setText(Time);
        
        
            }
        
        }catch(NumberFormatException c){JOptionPane.showMessageDialog(null, "Enter numeric value");}
    }//GEN-LAST:event_buttnSaveActionPerformed

