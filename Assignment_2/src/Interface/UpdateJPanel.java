    private void populateCarDetails(){
       
        int selectedrow =tblView.getSelectedRow();
        c = (Car) tblView.getValueAt(selectedrow, 0);
        
        
        
        textYrOfMfg.setText(String.valueOf(c.getManu_year()));
        textBrnd.setText(c.getBrnd());
        textCity.setText(c.getAvailbilityCity());
        textMaxSeating.setText(String.valueOf(c.getMaxNoseating()));
        textMinSeating.setText(String.valueOf(c.getMinSeating()));
        textModelNo.setText(c.getModelNo());
        textSerialNo.setText(String.valueOf(c.getSerialNo()));
        if(c.isAvb()==true)
            rbYesAvail.setSelected(true);
        
        else
            rbNoAvail.setSelected(true);
        
        if(c.isMaint_cert()==true)
            rbYesMaint.setSelected(true);
        else
            rbNoMaint.setSelected(true);
        
        
        
        
        
        
        
    }
    
    private void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel) tblView.getModel(); 
        dtm.setRowCount(0);
        for (Car car : carListInfo.getListCar()){
            Object[] row = new Object[10];
            row[0] = car;
            row[1] = car.getManu_year();
            row[2]= car.getMaxNoseating();
            row[3]= car.getMinSeating();
            row[4]=car.getSerialNo();
            row[5]=car.getModelNo();
            row[6]=car.getAvailbilityCity();
            row[7]=car.isAvb();
            row[8]=car.isMaint_cert();
            row[9]=car.getCreatOn();
            dtm.addRow(row);
            
    }
    }

